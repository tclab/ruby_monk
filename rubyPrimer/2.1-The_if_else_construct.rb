# Ruby conditional branching: the 'if' statement
def check_sign1(number)
	if number > 0
		"#{number} is positive"
	else 
		"#{number} is negative"
	end
end 
puts check_sign1(0)

def check_sign2(number)
  if number == 0
    number
  elsif number > 0
    "#{number} is positive"
  else
    "#{number} is negative"
  end        
end  
puts check_sign2(0)

